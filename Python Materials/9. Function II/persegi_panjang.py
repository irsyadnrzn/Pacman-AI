def luas_persegi_panjang():
    panjang = float(input("Masukkan Panjang Persegi Panjang: "))
    lebar = float(input("Masukkan Lebar Persegi Panjang: "))
    luas = panjang * lebar
    return luas 

def keliling_persegi_panjang():
    panjang = float(input("Masukkan Panjang Persegi Panjang: "))
    lebar = float(input("Masukkan Lebar Persegi Panjang: "))
    keliling = 2 * panjang + 2 * lebar
    return keliling 